# 3. Define variables for different Data Types int, Boolean, char, float, double and print on the
# Console.


# Python is dynamically-typed language, so we do not need to write the type of the data explicitly.

num = 21  # int
are_you_sure = True  # Boolean
letter = 'x'  # char/str
pi = 3.14  # float/double

print(num, type(num))
print(are_you_sure, type(are_you_sure))
print(letter, type(letter))
print(pi, type(pi))
