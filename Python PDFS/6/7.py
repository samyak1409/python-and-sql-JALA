# 7. Comparing strings.


print('Samyak' == 'Samyak')  # True
print('samyak' < 'Samyak')  # False
print('SAMYAK' > 'samyak')  # False
print('SAMYAK' <= 'SAMYAK')  # True
print('SAMYAK' >= 'samyak')  # False
print('SAMYAK' != 'samyak')  # True

str1, str2 = 'Sam', 'yak'
print(str1 is str1)  # True
print(str1 is not str2)  # True
